const reloadWebLayoutInternal = async (
  basedir: string,
  newLayouts: any,
) => {
  const list = await walkDir(basedir);

  for (let i of list) {
    const name = basename(
      i.endsWith(".tsx") ? i.substring(0, i.length - 4) : i,
    );
    newLayouts[name] = `() => import('../src/base/layout${
      i
        .substring(basedir.length, i.length - 4)
        .replace(/\\/gi, "/")
    }')`;
  }
};
